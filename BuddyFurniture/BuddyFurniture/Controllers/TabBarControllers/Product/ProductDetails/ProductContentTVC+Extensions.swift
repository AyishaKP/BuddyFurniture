import UIKit

extension ProductContentTVC {
    // MARK: - Table view data source
    
    override func numberOfSections(in tableView: UITableView) -> Int {
        return 1
    }
    
    override func tableView(_ tableView: UITableView, numberOfRowsInSection section: Int) -> Int {
        return arrayTitle.count
    }
    
    override func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
        let cell = tableView.dequeueReusableCell(withIdentifier: "SampleTableViewCell", for: indexPath) as? ProductExpandTableViewCell
        cell?.titleLabel.text = "Description"
        if let product = product {
            cell?.descriptionTextView.text = product.productDescription
        }
        return cell!
    }
}
